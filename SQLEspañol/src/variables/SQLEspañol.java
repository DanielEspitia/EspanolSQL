    private void LexicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LexicoActionPerformed
        controlError = 0;

        String c, resultado = " ";
        c = cadena.getText();
        StringTokenizer st = new StringTokenizer(c, " ");
        System.out.println(c);
        while (st.hasMoreTokens()) {

            String palabra = st.nextToken();
            if (Character.isDigit(palabra.charAt(0))) {
                n.checarNum(palabra);
                resultado = resultado + "*  " + palabra + "      " + n.obtenerNum() + "\n\n";
                if (n.obtenerValidador() == 1) {
                    controlError++;
                }
            } else { ///aqui me kede
                v.verificar(palabra);
                resultado = resultado + "*  " + palabra + "      " + v.obtenerVerificacion() + "\n\n";
                if (v.obtenerValidador() == 1) {
                    controlError++;
                }
            }
            // System.out.println(cont+" "+palabra);
            //cont++;

        }

        System.out.println("Control error: " + controlError);

            if (controlError == 0) {
                resultadosLexico.setForeground(Color.GREEN);
                resultadosLexico.setText(resultado);
                Sintactico.setEnabled(true);
            } else {
                resultadosLexico.setForeground(Color.RED);
                resultadosLexico.setText(resultado);
                Sintactico.setEnabled(false);
                resultadosSintactico.setText("");
                resultadosSemantico.setText("");
            }
        

        
    }//GEN-LAST:event_LexicoActionPerformed

