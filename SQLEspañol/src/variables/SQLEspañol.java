    private void SintacticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SintacticoActionPerformed
      Semantico.setEnabled(true);
        int control=0;
        
        String c, resultado = " ";

        c = cadena.getText();
        System.out.println(c);

        StringTokenizer st = new StringTokenizer(c, " ");

        String palabra = st.nextToken();

        if (palabra.toLowerCase().equals("actualizar")) {
            u.verificarUpdate(c);
            resultado = u.obtenerUpdate();
            control=u.obtenerValidador();
        } else if (palabra.toLowerCase().equals("borrar")) {
            d.Analizar_Delete(c);
            resultado = d.RevDelete();
            control=d.obtenerValidador();
        } else if (palabra.toLowerCase().equals("insertar")) {
            i.verificarinsert(c);
            resultado = i.obtenerVerificacion();
            control=i.obtenerValidador();
        } else if (palabra.toLowerCase().equals("seleccionar")) {
            s.AnalizarSeleccionar(c);
            resultado = s.obtenerSelect();
            control=s.obtenerValidador();
        } else {
            resultado = "Sentencia Incorrecta Dentro del Lenguaje se esperaba al inicio un:\n" + "\n* Insertar" + "\n* Seleccionar" + "\n* Actualizar" + "\n* Borrar";
        }

 if (control == 1) {
                resultadosSintactico.setForeground(Color.GREEN);
                resultadosSintactico.setText(resultado);
                Semantico.setEnabled(true);
            } else {
                resultadosSintactico.setForeground(Color.RED);
                resultadosSintactico.setText(resultado);
                Semantico.setEnabled(false);
                resultadosSemantico.setText("");
            }
    }//GEN-LAST:event_SintacticoActionPerformed

